# Cadenas método slicing

cadena = "Esto es una cadena de texto."

# Obtener un caracter a partir del índice de una cadena
print(cadena[0])  # E
print(cadena[1])  # s
print(cadena[2])  # t

# Obtener un rango de caracteres: cadena[inicio:fin]

esto = cadena[0:4]
print(esto)

# Obtener un rango de caracteres: cadena[inicio:fin:paso]

print(cadena[::2])

# Invertir cadena
print(cadena[::-1])
